# I need a function that calculates area of a circle

def areaOfCircle(radius):
    return 3.14 * radius * radius

# I need a function that calculates area of a square

def areaOfSquare(side):
    return side * side

# I need a function that calculates area of a rectangle

def areaOfRectangle(length, width):
    return length * width

# I need a function that calculates area of a triangle

def areaOfTriangle(base, height):
    return 0.5 * base * height